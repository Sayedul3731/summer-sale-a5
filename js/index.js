// card 1 
document.getElementById('card-1').addEventListener('click',function(){
    const cardOnePrice = getElementPriceById('card-1-price');
    const previousTotalPrice = getElementPriceById('total-price');
    const totalPrice = cardOnePrice + previousTotalPrice;
    const newTotalPrice = totalPrice.toFixed(2);
    setElementValueById('total-price', newTotalPrice);
    setBuyingProductTitle('buying-product-titles','card-1-title');    
});
